		uint8_t dat;
		uint8_t tmp[1] = { 0x00 };
		dat = 0x9E;
		GPIOC->ODR &= ~(1 << 15);	//reset cs
		HAL_SPI_Transmit(&hspi2, (uint8_t*) &dat, 1, 50);	//read ID command
		HAL_SPI_Receive(&hspi2, tmp, 1, 1000);
		MEM_ID = (uint32_t) tmp[0];
		MEM_ID <<= 8;                    			//MSB
		HAL_SPI_Receive(&hspi2, tmp, 1, 1000);
		MEM_ID += (uint32_t) tmp[0];
		MEM_ID <<= 8;
		HAL_SPI_Receive(&hspi2, tmp, 1, 1000);
		MEM_ID += (uint32_t) tmp[0];
		GPIOC->ODR |= 1 << 15;                    			// set cs
		return (MEM_ID);
